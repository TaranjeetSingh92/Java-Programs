public class Program7 {
    public static void main(String[] args) {
        for(int i=0;i<10;i++)
        {
            System.out.print("*"+" ");
        }
        System.out.println();
        int j=0;
        while(j<10)
        {
            System.out.print("+"+" ");
            j++;
        }
        System.out.println();
        int k=0;
        do
        {
            System.out.print("^"+" ");
            k++;
        }while(k<10);

    }
}
